    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            allFichas = new FichaDAO().buscarTodasFichasInstrutor(instrutor.getId());
            preencheTable(allFichas);
            
            ETipoExercicio[] e = ETipoExercicio.values();
            preencheCombo(e);
            
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel acessar o banco de dados para consultar exercicios. " + ex.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened

    private void jbSalvarExercicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarExercicioActionPerformed

        try{
            int row = jtFichaExercicio.getSelectedRow();
        
            Exercicio ex = new Exercicio();
            ex.setFicha(allFichas.get(row));
            ex.setNome(jtfNomeExercicio.getText());
            ex.setObservacao(jtaObservacao.getText());
            ex.setPeso(Double.parseDouble(jtfPeso.getText()));
            ex.setSeries((Integer) jsSeries.getValue());
            ex.setRepeticoes((Integer) jsRepeticoes.getValue());
            ex.setTipoExercicio((ETipoExercicio) jcbTipoExercicio.getSelectedItem());
            
            if(row < 0){
                JOptionPane.showMessageDialog(null, "Selecione uma ficha na tabela antes de salvar.");
                return;
            }
            
            if(jtfNomeExercicio.getText().equals("") || jtfPeso.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos antes de salvar.");
                return;
            }
            
            if(exercicio == null){
                new ExercicioDAO().inserir(ex);
                JOptionPane.showMessageDialog(null, "Exercicio cadastrado com sucesso.");
            } else {
                ex.setId(exercicio.getId());
                new ExercicioDAO().alterar(ex);
                JOptionPane.showMessageDialog(null, "Exercicio alterado com sucesso.");
                this.dispose();
            }

            jtfNomeExercicio.setText("");
            jtfPeso.setText("");
            jsRepeticoes.setValue(0);
            jsSeries.setValue(0);
            jtaObservacao.setText("");
            
            
        } catch (Exception exc) {
            JOptionPane.showMessageDialog(null, "Não foi possivel salvar exercicios, dados incorretos ou ficha não selecionada. ");
        }
        
    }//GEN-LAST:event_jbSalvarExercicioActionPerformed

