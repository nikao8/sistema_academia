    private void preencheCombo(ETipoExercicio[] e){
        DefaultComboBoxModel dcb = (DefaultComboBoxModel) jcbTipoExercicio.getModel();
        
        dcb.removeAllElements();
        
        for(ETipoExercicio te : e){
            dcb.addElement(te);
        }
        
        if(exercicio != null){
            jcbTipoExercicio.setSelectedItem(exercicio.getTipoExercicio());
        }
    }
    
    private void preencheTable(ArrayList<Ficha> allFichas) {
        DefaultTableModel dfm = (DefaultTableModel) jtFichaExercicio.getModel();
        
        while(dfm.getRowCount() > 0) {
            dfm.removeRow(0);
        }
        
        for(Ficha f : allFichas){
            
            String[] linha = {f.getAluno().getNome(),f.getTipoFicha().toString(),f.getDataCriacao().toString()};
            
            dfm.addRow(linha);
        }
        
    }

    void preparedEdit(Exercicio ex) {
        exercicio = ex;
        
       jtfNomeExercicio.setText(ex.getNome());
       jtfPeso.setText(ex.getPeso()+"");
       jtaObservacao.setText(ex.getObservacao());
       jsRepeticoes.setValue(ex.getRepeticoes());
       jsSeries.setValue(ex.getSeries());
       jcbTipoExercicio.setSelectedItem(ex.getTipoExercicio());
    }
