    private void preencheTable(ArrayList<Pagamento> allPagamentos) {
        DefaultTableModel dftm = (DefaultTableModel) jtPagamentos.getModel();
        
        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }
        
        for(Pagamento p : allPagamentos){
            String[] linha = {p.getTipoPagamento().toString(),p.getDataPagamento().toString(),p.getMatricula().getMensalidade()+""};
            dftm.addRow(linha);
        }
    }
