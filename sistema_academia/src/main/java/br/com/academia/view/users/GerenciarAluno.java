    private void preencheTable(ArrayList<Aluno> allAlunos) {
        DefaultTableModel dftm = (DefaultTableModel) jtAluno.getModel();
        
        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }
        
        for(Aluno a : allAlunos){
            String[] linha = {a.getNome(), a.getLogin(), a.getInstrutor().getNome()};
            dftm.addRow(linha);
        }
    }

