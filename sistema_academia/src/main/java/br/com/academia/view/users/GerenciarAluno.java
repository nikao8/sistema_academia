    private void jbAlterarAlunoActionPerformed(java.awt.event.ActionEvent evt) throws ParseException {//GEN-FIRST:event_jbAlterarAlunoActionPerformed
        int row = jtAluno.getSelectedRow();
        
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Selecione um Aluno na lista para alterar!");
        } else {
            InserirAluno insa = new InserirAluno(null,true);
            insa.setAcademia(academia);
            
            insa.preparedEdit(allAlunos.get(row));
            insa.setVisible(true);
        
            formWindowOpened(null);
        }
            
 
    }//GEN-LAST:event_jbAlterarAlunoActionPerformed

    private void jbApagarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbApagarAlunoActionPerformed
        int row = jtAluno.getSelectedRow();
        
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Selecione um aluno na lista para apagar!");
        } else {
            
            if(JOptionPane.showConfirmDialog(null, "Deseja apagar o aluno " + allAlunos.get(row).getNome() + "?",
                    "Apagar aluno?",
                    JOptionPane.YES_NO_OPTION) 
                    == JOptionPane.YES_OPTION ){
                try {
                    Aluno tmpAluno = allAlunos.get(row);
                    
                    new AlunoDAO().apagar(tmpAluno);
                    JOptionPane.showMessageDialog(null, "Aluno apagado com sucesso.");
                    formWindowOpened(null);
                } catch (SQLException | ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(null, "Não foi possivel apagar o aluno no banco de dados." + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jbApagarAlunoActionPerformed

