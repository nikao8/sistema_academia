    private void preencheTable(ArrayList<Exercicio> allExercicios) {
        DefaultTableModel dfm = (DefaultTableModel) jtExercicioFicha.getModel();
        
        while(dfm.getRowCount() > 0) {
            dfm.removeRow(0);
        }
        
        for(Exercicio e : allExercicios){
            Ficha f = e.getFicha();
            String[] linha = {f.getAluno().getNome(), f.getTipoFicha().toString(),
                             e.getNome(),e.getSeries()+"",e.getRepeticoes()+"",
                             e.getPeso()+"",e.getTipoExercicio().toString()};
            
            dfm.addRow(linha);
        }
        
    }
