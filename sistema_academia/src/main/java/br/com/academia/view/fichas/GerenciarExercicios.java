    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            allExercicios = new ExercicioDAO().buscarTodosExerciciosInstrutor(instrutor.getId());
            preencheTable(allExercicios);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel acessar o banco de dados para consultar exercicios. " + ex.getMessage());
        }
        
    }//GEN-LAST:event_formWindowOpened

