    private void jbApagarExercicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbApagarExercicioActionPerformed
        int row = jtExercicioFicha.getSelectedRow();
        
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um exercicio antes de apagar.");
        } else {
            if(JOptionPane.showConfirmDialog(null, "Deseja apagar o exercicio?",
                    "Apagar exercicio?",
                    JOptionPane.YES_NO_OPTION) 
                    == JOptionPane.YES_OPTION ){
                try {
                   
                    new ExercicioDAO().apagar(allExercicios.get(row));
                    JOptionPane.showMessageDialog(null, "Exercicio apagado com sucesso.");
                    formWindowOpened(null);
                } catch (SQLException | ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(null, "Não foi possivel apagar o exercicio no banco de dados. " + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jbApagarExercicioActionPerformed

    private void jbAlterarExercicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarExercicioActionPerformed
        int row = jtExercicioFicha.getSelectedRow();
        
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Selecione um exercicio antes de alterar.");
        } else {
            CadastrarExercicios ce = new CadastrarExercicios(null, true);
            ce.setInstrutor(instrutor);
            ce.preparedEdit(allExercicios.get(row));
            ce.setVisible(true);

            formWindowOpened(null);
        }
        

    }//GEN-LAST:event_jbAlterarExercicioActionPerformed

