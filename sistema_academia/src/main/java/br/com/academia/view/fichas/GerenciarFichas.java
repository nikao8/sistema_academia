    private void preencheTable(ArrayList<Ficha> allFichas){
        DefaultTableModel dtm = (DefaultTableModel) jtFicha.getModel();
        
        while(dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        
        for(Ficha f : allFichas) {
            try {
                int qtdEx = new FichaDAO().quantidadeExerciciosFicha(f.getId());
                String[] linha = { f.getAluno().getNome(), f.getTipoFicha().toString(), f.getDataCriacao().toString(), qtdEx+"" };    
                dtm.addRow(linha);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Não foi possivel acessar o banco de dados para retornar a quantidade de exercicios por ficha.");
            } 
        }
        
        
    }
