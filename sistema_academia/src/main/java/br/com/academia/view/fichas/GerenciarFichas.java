    private void jbAlterarFichaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarFichaActionPerformed
        int row = jtFicha.getSelectedRow();
        
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Selecione uma ficha para alterar! ");
        } else {
            CadastrarFichas cf = new CadastrarFichas(null, true);
            cf.setInstrutor(instrutor);
            cf.preparedEdit(allFichas.get(row));
            cf.setVisible(true);
        
            formWindowOpened(null);

        }
        

    }//GEN-LAST:event_jbAlterarFichaActionPerformed

    private void jbApagarFichaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbApagarFichaActionPerformed
        int row = jtFicha.getSelectedRow();
        if(jtFicha.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(null, "Selecione uma ficha para apagar! ");
        } else {
            
            if(JOptionPane.showConfirmDialog(null, "Deseja apagar a ficha?",
                    "Apagar Ficha?",
                    JOptionPane.YES_NO_OPTION) 
                    == JOptionPane.YES_OPTION ){
                try {
                    Ficha tmpF = allFichas.get(row);
                    tmpF.setExercicios(new ExercicioDAO().buscarTodosExerciciosFicha(tmpF.getId()));
                    new FichaDAO().apagar(tmpF);
                    JOptionPane.showMessageDialog(null, "Ficha apagada com sucesso.");
                    formWindowOpened(null);
                } catch (SQLException | ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(null, "Não foi possivel apagar a ficha no banco de dados. " + ex.getMessage());
                }
            }
        }
        
    }//GEN-LAST:event_jbApagarFichaActionPerformed

